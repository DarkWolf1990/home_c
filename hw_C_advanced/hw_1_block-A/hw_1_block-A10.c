/*
 * Распаковать порядок
Необходимо реализовать программу, которой на входе передаётся вещественное число в типе float, она возвращает порядок который хранится в поле EXP в виде десятичного целого числа. Функция должна строго соответствовать прототипу

 int extractExp(float)

 Примеры
Входные данные
 1.0

 Результат работы:
 127

 Входные данные
 2.0

 Результат работы:
 128

 Входные данные
 3.99999

 Результат работы:
 128

 Входные данные
 0.001

 Результат работы:
 117
 * */

#include <stdio.h>
#include <stdint.h>

int extractExp(float x) {
    // Преобразуем указатель на float в указатель на uint32_t
    uint32_t* ptr = (uint32_t*)&x;

    // Извлекаем поле экспоненты (биты 23-30)
    uint32_t exp_bits = (*ptr >> 23) & 0xFF;

    return (int)exp_bits;
}

int main() {
    float x;
    scanf("%f", &x);
    int result = extractExp(x);
    printf("%d", result);
    return 0;
}