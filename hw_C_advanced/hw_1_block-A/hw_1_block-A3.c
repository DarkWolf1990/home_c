/*
 * Извлечение бит
На вход программе подается беззнаковое 32-битное целое число N и натуральное число K (1 ≤ K ≤ 31).
 Требуется взять K младших битов числа N и вывести полученное таким образом число.

Примеры:
Входные данные
 2 1

 Результат работы:
 0

 Входные данные
 12 3

 Результат работы:
 4

 Входные данные
 3 5

 Результат работы:
 3

 Входные данные
 1024 3

 Результат работы:
 0
 */

#include <stdio.h>

int main(void) {
    unsigned int N = 0;
    int K = 0;
    scanf("%u %d", &N, &K);

    // Нормализуем K в диапазоне 1-31
    if (K < 1) {
        K = 1;
    } else if (K > 31) {
        K = 31;
    }

    // Создаем маску для K младших битов
    unsigned int mask = (1U << K) - 1;

    // Применяем маску к числу N
    unsigned int result = N & mask;

    printf("%u", result);

    return 0;
}