/*
 * Поиск последовательности бит
На вход программе подается беззнаковое 32-битное целое число N и натуральное число K (1 ≤ K ≤ 31). Требуется взять K подряд идущих битов числа N так, чтобы полученное число было максимальным. Программа должна вывести полученное число.

Примеры
Входные данные
 2 1

 Результат работы:
 1

 Входные данные
 12 3

 Результат работы:
 6

 Входные данные
 3 5

 Результат работы:
 3

 Входные данные
 1024 3

 Результат работы:
4
 */

#include <stdio.h>

int main(void) {
    unsigned int N = 0;
    int K = 0;
    scanf("%u %d", &N, &K);

    // Нормализуем K в диапазоне 1-31
    if (K < 1) K = 1;
    if (K > 31) K = 31;

    unsigned int max_value = 0;
    unsigned int mask = (1U << K) - 1;

    // Проходим по всем возможным позициям K подряд идущих битов
    for (int i = 0; i <= 32 - K; i++) {
        // Сдвигаем маску на i позиций влево и применяем к N
        unsigned int current = (N >> i) & mask;
        if (current > max_value) {
            max_value = current;
        }
    }

    printf("%u", max_value);
    return 0;
}