/*
Сколько раз встречается максимум
На стандартном потоке ввода задаётся натуральное число N (N > 0),
после которого следует последовательность из N целых чисел.
На стандартный поток вывода напечатайте,
сколько раз в этой последовательности встречается максимум.
Указание: использовать массивы запрещается.

Примеры
Входные данные:
6 1 2 3 2 0 3
Выходные данные:
2
 */


#include <stdio.h>

int main() {
    int n, current, max, count = 0;

    scanf("%d", &n);

    // Обрабатываем первое число отдельно
    if (n > 0) {
        scanf("%d", &current);
        max = current;
        count = 1;

        // Обрабатываем остальные числа
        for (int i = 1; i < n; i++) {
            scanf("%d", &current);

            if (current > max) {
                max = current;
                count = 1;
            } else if (current == max) {
                count++;
            }
        }
    }

    printf("%d", count);
    return 0;
}